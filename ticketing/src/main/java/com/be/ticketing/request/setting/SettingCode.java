package com.be.ticketing.request.setting;

public class SettingCode {
	private String code;

	public SettingCode(String code) {
		super();
		this.code = code;
	}
	
	public SettingCode() {
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}
	
	
}
